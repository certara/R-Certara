# Tests for write_mcp_config(): JSON merge/idempotency and Codex snippet.

test_that("cursor project config is written and merges idempotently", {
  proj <- file.path(tempdir(), paste0("mcpcfg_", as.integer(Sys.time())))
  dir.create(proj, showWarnings = FALSE, recursive = TRUE)

  # Pre-seed an unrelated server to prove we merge, not overwrite.
  cfg_path <- file.path(proj, ".cursor", "mcp.json")
  dir.create(dirname(cfg_path), recursive = TRUE, showWarnings = FALSE)
  jsonlite::write_json(list(mcpServers = list(browsermcp = list(command = "x"))),
                       cfg_path, auto_unbox = TRUE)

  write_mcp_config(client = "cursor", scope = "project", project_dir = proj)
  cfg <- jsonlite::read_json(cfg_path)
  expect_true(!is.null(cfg$mcpServers$browsermcp))
  expect_true(!is.null(cfg$mcpServers[["certara-r"]]))
  expect_match(cfg$mcpServers[["certara-r"]]$command, "Rscript")
  expect_identical(cfg$mcpServers[["certara-r"]]$type, "stdio")
  launch <- cfg$mcpServers[["certara-r"]]$args[[2]]
  expect_match(launch, "server_name = 'certara-r'")

  rule_path <- file.path(proj, ".cursor", "rules", "certara-mcp-usage.mdc")
  expect_true(file.exists(rule_path))
  rule_txt <- paste(readLines(rule_path, warn = FALSE), collapse = "\n")
  expect_match(rule_txt, "user-certara-r")
  expect_match(rule_txt, "auto-generated by Certara.R::write_mcp_config")

  perm_path <- file.path(proj, ".cursor", "permissions.json")
  expect_true(file.exists(perm_path))
  perms <- jsonlite::read_json(perm_path)
  expect_identical(unlist(perms$mcpAllowlist, use.names = FALSE), "certara-r:*")

  # Idempotent: second write does not duplicate / change server count.
  write_mcp_config(client = "cursor", scope = "project", project_dir = proj)
  cfg2 <- jsonlite::read_json(cfg_path)
  expect_length(cfg2$mcpServers, 2)
})


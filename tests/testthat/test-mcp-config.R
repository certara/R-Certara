# Point .claude_desktop_config_dir() at a temp tree (no MSIX package) per OS, so
# the merge/guidance tests run cross-platform.
desktop_env <- function(dir) {
  if (.Platform$OS.type == "windows") {
    empty <- file.path(dir, "local_empty")
    dir.create(empty, showWarnings = FALSE, recursive = TRUE)
    c(LOCALAPPDATA = empty, APPDATA = dir)
  } else {
    c(HOME = dir)
  }
}

test_that("claude-desktop writes and merges claude_desktop_config.json", {
  root <- tempfile("desktoproot_")
  dir.create(root)

  withr::with_envvar(desktop_env(root), {
    cfg_path <- .claude_desktop_config_path()
    dir.create(dirname(cfg_path), showWarnings = FALSE, recursive = TRUE)
    # Pre-seed an unrelated server to prove we merge, not overwrite.
    jsonlite::write_json(list(mcpServers = list(filesystem = list(command = "x"))),
                         cfg_path, auto_unbox = TRUE)

    acts <- suppressMessages(write_mcp_config(client = "claude-desktop"))

    cfg <- jsonlite::read_json(cfg_path)
    expect_true(!is.null(cfg$mcpServers$filesystem))
    expect_true(!is.null(cfg$mcpServers[["certara-r"]]))
    expect_identical(cfg$mcpServers[["certara-r"]]$type, "stdio")
    expect_match(cfg$mcpServers[["certara-r"]]$command, "Rscript")
    # Desktop gets the long (600s) watch budget and no per-server timeout key.
    expect_match(cfg$mcpServers[["certara-r"]]$args[[2]],
                 "job_watch_wait_seconds = 600")
    expect_null(cfg$mcpServers[["certara-r"]]$timeout)

    dt <- Filter(function(a) identical(a$client, "claude-desktop"), acts)[[1]]
    expect_identical(dt$scope, "user")
    expect_identical(normalizePath(dt$written, mustWork = FALSE),
                     normalizePath(cfg_path, mustWork = FALSE))

    # Idempotent: second write keeps two servers, no duplication.
    suppressMessages(write_mcp_config(client = "claude-desktop"))
    expect_length(jsonlite::read_json(cfg_path)$mcpServers, 2)
  })
})


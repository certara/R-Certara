  dir.create(file.path(proj, ".claude"), showWarnings = FALSE, recursive = TRUE)
  set_path <- file.path(proj, ".claude", "settings.json")
  # Pre-seed unrelated settings to prove we merge, not clobber.
  jsonlite::write_json(
    list(model = "opus", permissions = list(allow = list("Bash(ls:*)"))),
    set_path, auto_unbox = TRUE
  )

  suppressMessages(
    write_mcp_config(client = "claude-code", scope = "project", project_dir = proj)
  )
  set <- jsonlite::read_json(set_path)
  allow <- unlist(set$permissions$allow, use.names = FALSE)
  expect_true(all(c("Bash(ls:*)", "mcp__certara-r") %in% allow))
  expect_identical(set$model, "opus")

  acts <- suppressMessages(
    remove_mcp_config(client = "claude-code", scope = "project", project_dir = proj)
  )
  cc <- Filter(function(a) identical(a$client, "claude-code"), acts)[[1]]
  expect_true(isTRUE(cc$permissions_removed))
  set2 <- jsonlite::read_json(set_path)
  allow2 <- unlist(set2$permissions$allow, use.names = FALSE)
  expect_false("mcp__certara-r" %in% allow2)        # ours gone
  expect_true("Bash(ls:*)" %in% allow2)             # unrelated preserved
  expect_identical(set2$model, "opus")              # other keys preserved
})


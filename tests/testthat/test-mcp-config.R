test_that("remove_mcp_config drops our server but preserves others", {
  proj <- file.path(tempdir(), paste0("mcpcfg_rm_", as.integer(Sys.time())))
  dir.create(proj, showWarnings = FALSE, recursive = TRUE)
  cfg_path <- file.path(proj, ".cursor", "mcp.json")
  dir.create(dirname(cfg_path), recursive = TRUE, showWarnings = FALSE)
  jsonlite::write_json(list(mcpServers = list(browsermcp = list(command = "x"))),
                       cfg_path, auto_unbox = TRUE)

  write_mcp_config(client = "cursor", scope = "project", project_dir = proj)
  expect_true(!is.null(jsonlite::read_json(cfg_path)$mcpServers[["certara-r"]]))

  rule_path <- file.path(proj, ".cursor", "rules", "certara-mcp-usage.mdc")
  expect_true(file.exists(rule_path))

  acts <- remove_mcp_config(client = "cursor", scope = "project",
                            project_dir = proj)
  cfg <- jsonlite::read_json(cfg_path)
  expect_null(cfg$mcpServers[["certara-r"]])
  expect_true(!is.null(cfg$mcpServers$browsermcp))
  expect_true(acts[[1]]$removed)
  expect_true(acts[[1]]$rule_removed)
  expect_false(file.exists(rule_path))
  expect_true(acts[[1]]$permissions_removed)
  expect_false(file.exists(file.path(proj, ".cursor", "permissions.json")))

  # Removing again is a no-op that reports nothing was present.
  acts2 <- remove_mcp_config(client = "cursor", scope = "project",
                             project_dir = proj)
  expect_false(acts2[[1]]$removed)
})


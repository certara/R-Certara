test_that("claude-code installs and removes a CLAUDE.md-imported guidance doc", {
  proj <- file.path(tempdir(), paste0("mcpcfg_ccguide_", as.integer(Sys.time())))
  dir.create(proj, showWarnings = FALSE, recursive = TRUE)
  # Pre-seed CLAUDE.md to prove the managed block is appended, not clobbered.
  claude_md <- file.path(proj, "CLAUDE.md")
  writeLines(c("# Project notes", "", "Keep me."), claude_md)

  acts <- suppressMessages(
    write_mcp_config(client = "claude-code", scope = "project", project_dir = proj)
  )
  cc <- Filter(function(a) identical(a$client, "claude-code"), acts)[[1]]

  doc <- file.path(proj, ".claude", "certara-mcp-usage.md")
  expect_true(file.exists(doc))
  doc_txt <- paste(readLines(doc, warn = FALSE), collapse = "\n")
  expect_match(doc_txt, "certara_mcp_capabilities")        # session-start nudge
  expect_match(doc_txt, "auto-generated by Certara.R::write_mcp_config")
  expect_identical(cc$guidance_written$doc, doc)

  md <- readLines(claude_md, warn = FALSE)
  expect_true(any(grepl("@.claude/certara-mcp-usage.md", md, fixed = TRUE)))
  expect_true(any(grepl("Keep me.", md, fixed = TRUE)))     # user content kept

  # Idempotent: a second write must not duplicate the import block.
  suppressMessages(
    write_mcp_config(client = "claude-code", scope = "project", project_dir = proj)
  )
  md2 <- readLines(claude_md, warn = FALSE)
  expect_length(grep("@.claude/certara-mcp-usage.md", md2, fixed = TRUE), 1)

  # Removal: doc deleted, block stripped, user content preserved.
  rm <- suppressMessages(
    remove_mcp_config(client = "claude-code", scope = "project", project_dir = proj)
  )
  cc_rm <- Filter(function(a) identical(a$client, "claude-code"), rm)[[1]]
  expect_true(isTRUE(cc_rm$guidance_removed))
  expect_false(file.exists(doc))
  md3 <- readLines(claude_md, warn = FALSE)
  expect_false(any(grepl("certara-mcp-usage.md", md3, fixed = TRUE)))
  expect_true(any(grepl("Keep me.", md3, fixed = TRUE)))
})

